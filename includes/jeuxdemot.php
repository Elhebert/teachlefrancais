			<legend>Les jeux avec les mots</legend>
			<b><u>L'anagramme</u> :</b> <i>Une anagramme consiste à modifier l'ordre des lettres dans un mot pour en changer le sens</i>.<br>
			<u>Ex</u> : Rien -> nier, aigle -> agile, grenat -> argent, gardienne -> grenadine<br>
			<br>
			<b><u>La Contrepètrie</u> : </b> <i>La contrepèterie consiste à écrire une phrase qui, si l'on permute au moins deux des lettres ou des syllabes qui la composent, produit une autre phrase de sens différent et si possible coquin, voire obscène</i>.<br>
			<u>Ex</u> : C'est un <u>m</u>inistre dé<u>c</u>ent -> C'est un <u>c</u>inistre dé<u>m</u>ent (lire : C'est un sinistre dément)<br>
			<br>
			<b><u>L'antithèse</u> : </b> <i>L'antithèse consiste à rapprocher dans un même énonce deux mots ou deux idées qui s'opposent par le sens</i>.<br>
			<u>Ex</u> : "<u>Être</u> ou <u>ne pas être</u>, voilà la question" <b>Shakespear</b>, <i>Hamlet</i><br>
			<br>
			<b><u>L'oxymore</u> : </b> <i>L'oxymore (ou alliance de mots) consiste à juxtaposer deux mots de sens contraire, que l'on n'à donc pas l'habitude de trouver accolés</i>.<br>
			<u>Ex</u> : Un illustre inconnu, un silence éloquent, se hâter lentement<br>
			<br><b>
			<u>L'hyperbole</u> : </b> <i>L'hyperbole consiste à employer des mots très forts qui vont au-delà de la pensée. C'est l'expression exagérée ou amplifiée d'une idée ou d'un fait</i>.<br>
			<u>Ex</u> : Verser un torrent de larme<br>
			<br><b>
			<u>La gradation</u> : </b> <i>la gradation est constituée d'une succession de mots ou d'idée de sens proche, rangés en ordre croissant ou décroissant d'intensité</i>.<br>
			<u>Ex</u> : "Va, cours, vole, et nous venges !" <b>Corneille</b>, <i>Le Cid</i><br>
			<br><b>
			<u>L'anacoluthe</u> : </b> <i>L'anacoluthe est une rupture de construction syntaxique, c'est-à-dire une transformation, au milieu d'une phrase, de la construction grammaticale que le début de cette phrase laissait attendre</i>.<br>
			<u>Ex</u> : "Le nez de Cléopâtre, s'il eût été plus court, toute la face de la terre aurait changé" <b>Pascal</b>, <i>Pensées</i><br>
			<br>
			<b><u>L'anaphore</u> : </b> <i>L'anaphore est la répétition d'un même mot ou d'une même construction au début de vers, de phrase ou memebres de phrases successif</i>.<br>
			<u>Ex</u> : "<u>Toujours</u> aimer, <u>toujours</u> souffrir, <u>toujours</u> mourir" <b>Corneille</b><br>
