			<legend>Les figures rhétoriques</legend>
			<b><u>La comparaison</u> :</b> <i>La comparaison rapproche deux termes, au moyen d'un mot, pour insister sur les rapports de ressemblance qui les unissent</i>.<br>
			<u>Ex</u> : Il est têtu comme un âne.<br>
			<br>
			<b><u>La métaphore</u> : </b> <i>La métaphore assimile  deux termes pour insister sur les rapports de ressemblance qui les unissent mais, à la différence de la comparaison, le comparatif est absent</i>.<br>
			<u>Ex</u> : Elle a une taille de guêpe<br>
			<br>
			<b><u>L'allégorie</u> : </b> <i>L'allégorie consiste à rendre concrète une abstraction, c'est-à-dire à représenter de façon imagée, un sentiment, une qualité morale ou une force la nature</i>.<br>
			<u>Ex</u> : Représenter la mort sous la forme d'un vieille femme munie d'une faux est une allégorie<br>
			<br>
			<b><u>La métonymie</u> : </b> <i>La métonymie consiste à ne pas désigner un être ou un objet par son nom mais par un autre nom qui est lié au premier un rapport logique</i>.<br>
			<u>Ex</u> : Une belle main (pour une belle écriture)<br>
			<br><b>
			<u>Synecdoque</u> : </b> <i>La synecdoque consiste à remplacer le nom d'un être ou d'une chose, non par le nom d'une de ses caractéristiques, mais par celui d'une de ses parties</i>.<br>
			<u>Ex</u> : Une voile pour un bateau à voile<br>
			<br><b>
			<u>La périphrase</u> : </b> <i>La périphrase consiste à dire en plusieurs mots ce qui pourrait être dit en un seul.</i>.<br>
			<u>Ex</u> : Le billet vert pour le dollar<br>
			<br>
