					<legend class="ref">Serveurs et protocoles.</legend>
						<h3><u>Serveurs</u> :</h3>
						<img style="border: 1px solid" src="includes/images/serv.jpg" alt="serveur">
						Un serveur informatique est un dispositif informatique matériel ou logiciel qui offre des services, à différents clients. Les services les plus courants 
						sont : le partage de fichiers, l'accès aux informations du World Wide Web, le courrier électronique, le partage d'imprimantes, le commerce électronique, le stockage en base de 
						données, le jeu et la mise à disposition de logiciels applicatifs (optique software as a service).
						<br>
						Un serveur fonctionne en permanence, répondant automatiquement à des requêtes provenant d'autres dispositifs informatiques (les clients), selon le principe dit client-serveur. Le 
						format des requêtes et des résultats est normalisé, se conforme à des protocoles réseaux et chaque service peut-être exploité par tout client qui met en œuvre le protocole propre 
						à ce service.
						<br>
						Les serveurs sont utilisés par les entreprises, les institutions et les opérateurs de télécommunication. Ils sont courants dans les centres de traitement de données et le réseau 
						Internet.
						<br>
						<h3><u>Protocoles</u> :</h3>
						<img style="border: 1px solid" src="includes/images/protocole.jpg" width="150px">Dans les réseaux informatiques et les télécommunications, un protocole de communication est une spécification de plusieurs règles pour un type de communication particulier. 
						Initialement, on nommait protocole ce qui est utilisé pour communiquer sur une même couche d'abstraction entre deux machines différentes. Par extension de langage, on utilise 
						parfois ce mot aussi aujourd'hui pour désigner les règles de communication entre deux couches sur une même machine. Les protocoles de communication les plus utilisés sont les 
						protocoles réseau.
						<br>
						<h3><u>Exemple de protocoles</u> :</h3>
						* <b><u>Ethernet</u></b> (aussi connu sous le nom de norme IEEE 802.3) est un standard de transmission de données pour réseau local basé sur le principe suivant : 
						<div class="citation">
							Toutes les machines du réseau Ethernet 
							sont connectées à une même ligne de communication, 
							constituée de câbles cylindriques
						</div>
						<br>
						* <b><u>Secure Shell (SSH)</u></b> est à la fois un programme informatique et un protocole de communication sécurisé. Le protocole de connexion impose un échange de clés de 
						chiffrement en début de connexion. Par la suite toutes les trames sont chiffrées. Il devient donc impossible d'utiliser un sniffer pour voir ce que fait l'utilisateur. Le 
						protocole SSH a été conçu avec l'objectif de remplacer les différents programmes rlogin, telnet et rsh.
						<br><br>
						* Le <b><u>Simple Mail Transfer Protocol</u></b> (littéralement « Protocole simple de transfert de courrier »), généralement abrégé SMTP, est un protocole de communication 
						utilisé pour transférer le courrier électronique (courriel) vers les serveurs de messagerie électronique. SMTP est un protocole assez simple (comme son nom l'indique). On 
						commence par spécifier l'expéditeur du message puis, le ou les destinataires d'un message, puis, en général après avoir vérifié leur existence, le corps du message est transféré. 
						Il est assez facile de tester un serveur SMTP en utilisant la commande telnet sur le port 25 d'un serveur distant.
						<br><br>
						* <b><u>Internet Protocol</u></b> (abrégé en IP) est une famille de protocoles de communication de réseau informatique conçu pour et utilisés par Internet. Les protocoles IP 
						sont au niveau 3 dans le modèle OSI. Les protocoles IP s'intègrent dans la suite des protocoles Internet et permettent un service d'adressage unique pour l'ensemble des terminaux 
						connectés.
